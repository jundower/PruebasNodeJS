async function regenerar_configurar_tablas(modulo){
    var mensaje="Campos del "+modulo+" restablecidos";
    var columnas=columnas_configurar_tablas[modulo];

    await $.ajax({
        type: 'POST',
        url: url_restablecer_campos,
        data:{
            columnas:JSON.stringify( columnas ),
            modulo: modulo
        },
        success: function (lists){
            ((lists >= 1) ? generar_configurar_tablas(modulo) : mensaje= "Ocurrió un problema cuando se restableció el "+modulo);
        },
        error: function(XMLHttpRequest, textStatus, errorThrown) { 
            mensaje = "Ocurrió un problema al restablecer "+modulo ;
        } 
    });
    return mensaje + '<br>';
}

