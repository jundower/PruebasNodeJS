async function guardar_configurar_tablas(modulo){
    var mensaje=validar_configurar_tablas(modulo);
    if(mensaje==""){
        
        listados_configurar_tablas[modulo].forEach(list=>{
            var visible = ($("#"+list.modulo+'_'+list.campo+"_visible").prop('checked') == true ? 0 : 1 );
            var posicion = $("#"+list.modulo+'_'+list.campo+"_posicion").val();
            var width = $("#"+list.modulo+'_'+list.campo+"_width").val();
            list.nueva_posicion=posicion;
            list.visible=visible;
            list.width=width;
        });

        await $.ajax({
            type: 'POST',
            url: url_guardar_campos,
            data: {
                lista: JSON.stringify( listados_configurar_tablas[modulo] )
            },
            success: function (lists){
                generar_configurar_tablas(modulo);
                mensaje="Campos del listado de "+modulo+" guardados exitosamente";
            },
            error: function(XMLHttpRequest, textStatus, errorThrown) { 
                mensaje="Campos del listado de "+modulo+" no se pudieron guardar";
            } 
        });
    }
    return mensaje +'<br>';
}

function validar_configurar_tablas(modulo){

    var result=true;
    var mensaje="Verificar los siguientes campos en listado de "+modulo+" : ";
    var posiciones=[];
    listados_configurar_tablas[modulo].forEach(list=>{
        var posicion = $("#"+list.modulo+'_'+list.campo+"_posicion").val();
        posiciones.push(posicion);
        if(posicion == 0 || posicion > listados_configurar_tablas[modulo].length ){
            mensaje += "<br>Fuera de rango: " + list.campo ;
            result = false;
        }
    });   

    for(var i=0 ; i<posiciones.length; i++){
        var element = posiciones[i];
        var idx = posiciones.indexOf(element);
        if(idx != i){
            mensaje += "<br>Ordenes repetidos: " + element ;
            result = false;
        }
    }

    if(result){
        mensaje="";
    }
    return mensaje;
}

async function guardar(){
    var mensaje="";
    var columnas=[];
    await $.each(columnas_configurar_tablas, async function(key, value) {
        columnas.push(key);
    });
    for (const item of columnas) {
        mensaje += await guardar_configurar_tablas(item);
    }
    return mensaje
}


